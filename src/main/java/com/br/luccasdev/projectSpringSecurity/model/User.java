package com.br.luccasdev.projectSpringSecurity.model;


import jakarta.persistence.*;

import java.util.Set;
import java.util.UUID;

@Entity
@Table(name = "tb_users")
public class User {

    @Id
    @GeneratedValue(strategy = GenerationType.UUID)
    @Column(name = "user_id")
    private UUID userId;

    @Column(unique = true)
    private String username;
    private String password;

    @ManyToMany(cascade = CascadeType.ALL, fetch = FetchType.EAGER) // o fetch serve para ele trazer a role do banco de dados runtime
    @JoinTable(
            name = "tb_user_roles", // nome da tabela intermediaria
            joinColumns = @JoinColumn(name = "user_id"), // nome da coluna que tem a PK
            inverseJoinColumns = @JoinColumn(name = "role_id") // nome da coluna que tem a FK
    )
    private Set<Role> roles;

    public UUID getUserId() {
        return userId;
    }

    public void setUserId(UUID userId) {
        this.userId = userId;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }

    public Set<Role> getRoles() {
        return roles;
    }

    public void setRoles(Set<Role> roles) {
        this.roles = roles;
    }
}
